/**
 * Retrieves an item definition by server item ID.
 */
export function getItemDef(id: number): ItemDef | undefined {
    return ITEM_DEFS.get(id);
}

/**
 * Retrieves an item definition by name (case-insensitive).
 */
export function getItemDefByName(name: string): ItemDef | undefined {
    const lower = name.toLowerCase();
    for (const def of ITEM_DEFS.values()) {
        if (def.name.toLowerCase() === lower) return def;
    }
    return undefined;
}

/**
 * Returns all item definitions of a given type.
 */
export function getItemDefsByType(type: ItemType): ItemDef[] {
    const result: ItemDef[] = [];
    for (const def of ITEM_DEFS.values()) {
        if (def.type === type) result.push(def);
    }
    return result;
}

