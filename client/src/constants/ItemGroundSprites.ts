/** Lookup map built from RAW_DATA. */
const GROUND_SPRITE_MAP = new Map<number, GroundSpriteInfo>(
  RAW_DATA.map(([id, sheet, frame]) => [id, { sheet, frame }])
);

/** Default ground sprite for unknown items. */
const DEFAULT_GROUND_SPRITE: GroundSpriteInfo = { sheet: 0, frame: 0 };

/**
 * Get the ground sprite atlas frame info for a given server item ID.
 * Returns { sheet, frame } to build frame name `spr${sheet}_f${frame}`.
 */
export function getItemGroundFrame(itemId: number): GroundSpriteInfo {
  return GROUND_SPRITE_MAP.get(itemId) ?? DEFAULT_GROUND_SPRITE;
}
